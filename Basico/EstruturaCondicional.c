#include <stdio.h>

int main() {
    int numero = 5;

    // Verifica se o número é maior, igual ou menor que 5
    if (numero > 5) {
        printf("O número é maior que 5.\n");
    } else if (numero == 5) {
        printf("O número é igual a 5.\n");
    } else {
        printf("O número é menor que 5.\n");
    }

    return 0;
}
